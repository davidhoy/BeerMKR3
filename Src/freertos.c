void StartDefaultTask(void const * argument)
{

  /* USER CODE BEGIN StartDefaultTask */
  sflash_handle_t hflash = { 0 };
  int ret = init_sflash(&hflash, NULL, SFLASH_WRITE_ALLOWED);
  if (ret == 0)
  {
	  sprintf(msg, "SPI flash successfully initialized, device=0x%04x\r\n", (unsigned int)hflash.device_id);
  }
  else
  {
	  sprintf(msg, "SPI flash NOT initialized, ret=%d\r\n", ret);
  }
  HAL_UART_Transmit_DMA(&huart1, (uint8_t*)msg, strlen(msg));

  /* Infinite loop */
  int i = 0;
  unsigned long address = 0;
  while (1)
  {
    osDelay(1000);
  	sprintf(msg, "Hello world %d\r\n", i++);
	HAL_UART_Transmit_DMA(&huart1, (uint8_t*)msg, strlen(msg));
	osDelay(2);
#if 1
	ret = sflash_read(&hflash, address, buffer, sizeof(buffer));
	if (ret == 0)
	{
		msg[0] = '\0';
		for (int j = 0; j < sizeof(buffer); j++)
		{
			int offset = strlen(msg);
			sprintf(&msg[offset], "%02x ", buffer[j]);
		}
		strcat(msg,"\r\n");
		HAL_UART_Transmit_DMA(&huart1, (uint8_t*)msg, strlen(msg));
		address += sizeof(msg);
	}
#endif

  }

  /* USER CODE END StartDefaultTask */
}

